// ── Dual graph code construction ──────────────────────────────────────────
function SboxCode(sb, n, N)
    k := n + 1;
    H := ZeroMatrix(GF(2), k, N);
    for x := 0 to N-1 do H[1][x+1] := 1; end for;
    for x := 0 to N-1 do
        for i := 0 to n-1 do
            H[i+2][x+1] := (x div 2^i) mod 2;
        end for;
    end for;
    for x := 0 to N-1 do
        for j := 0 to n-1 do
            H[n+1+j+1][x+1] := (sb[x+1] div 2^j) mod 2;
        end for;
    end for;
    return Dual(LinearCode(H));
end function;

